#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2)
	{
		while (*s1 != 0 && *s2 != 0 && *s1 == *s2 && n)
		{
			s1++;
			s2++;
			n--;
		}
		if (*s1 != *s2 && n)
			return (0);
		else
			return (1);
	}
	return (-1);
}
